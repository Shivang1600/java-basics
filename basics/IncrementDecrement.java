public class IncrementDecrement {
    public static void main(String[] args) {
        int a = 5;

        System.out.println("a: " + a);    // Output: 5
        System.out.println("a++: " + a++); // Output: 5
        System.out.println("++a: " + ++a); // Output: 7
        System.out.println("a--: " + a--); // Output: 7
        System.out.println("--a: " + --a); // Output: 5
    }
}
